#include "../includes/libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	size_t i;

	i = 0;
	while (i < n)
	{
		ft_memset((void*)src++, (int)dest++, 1);
		i++;
	}
	return (dest);
}
